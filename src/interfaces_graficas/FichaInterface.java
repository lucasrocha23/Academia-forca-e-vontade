    public String geraMatricula(){
        Calendar rightNow = Calendar.getInstance();
        int dia  = rightNow.get(Calendar.DAY_OF_YEAR);
        int hora = rightNow.get(Calendar.HOUR_OF_DAY);
        int min  = rightNow.get(Calendar.MINUTE);
        int intR = (int) (Math.random() * 10);

        String strDia  = Integer.toString(dia);
        String strHr   = Integer.toString(hora);
        String strMin  = Integer.toString(min);
        String strRand = Integer.toString(intR);
        
        if (dia < 100) strDia = "0" + strDia;
        if (dia < 10)  strDia = "0" + strDia;
        if (hora < 10) strHr  = "0" + strHr;
        if (min < 10)  strMin = "0" + strMin;
        
        String numMatricula = strDia + strHr + strMin + strRand;
        return numMatricula;
    }
    
